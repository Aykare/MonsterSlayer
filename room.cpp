int Room::stayEventTrigger() {
    //srand(time(NULL));
    int event = rand() % 100 + 1;
    int triggered;
    if (event<=20) {
        //treasure event
        triggered = 1;
    }
    else if (event>20&&event<=60) {
        //nothing
        triggered = 2;
    }
    else if (event>60) {
        //battle
        triggered = 3;
    }
    else {
        cout << "This should not be possible. An error has occured." << endl;
        triggered = 0;
    }
    return triggered;
}

